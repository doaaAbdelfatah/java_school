public class Teacher {

    String name ;
    String gender;
    int age;

    public Teacher() {

    }

    public Teacher(String name, String gender) {
        this.name = name;
        this.gender = gender;
    }

    public Teacher(String name, String gender, int age) {
        this.name = name;
        this.gender = gender;
        this.age = age;
    }
}
